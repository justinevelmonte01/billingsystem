    private void addAdmin(){
         try(Connection conn = Mysql_Connect.getConnection()){
            String sqlSelectClients = "SELECT * FROM admin WHERE userName = ?";
            String sqlAdd = "INSERT INTO admin(userName, password) VALUES (?, ?)";
            
            PreparedStatement stmtClients = conn.prepareStatement(sqlSelectClients); 
            PreparedStatement stmtAdd = conn.prepareStatement(sqlAdd); 
            
            stmtClients.setString(1, username.getText());
            
            ResultSet resultClients = stmtClients.executeQuery();
            
            if(!resultClients.next()){
                stmtAdd.setString(1, username.getText());
                stmtAdd.setString(2, password.getText());
                
                int result = stmtAdd.executeUpdate();
                
                if(result > 0){
                    JOptionPane.showMessageDialog(null, "Add completed successfully!");
                    
                    Dashboard DashboardFrame = new Dashboard();
                    DashboardFrame.setVisible(true);
                }
            }else{
                JOptionPane.showMessageDialog(null, "User Name Taken", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
